class Solution {
public:
    void moveZeroes(vector<int>& nums) {
        int size = nums.size();
        for(int i = 0;i<size;i++)
        {
            

       
         for(int j= i+1;j<size;j++)
         {
          if(nums[i]==0)
          {
              swap(nums[i],nums[j]);
}
           
            
                
}
               
                
}
  
            
            
}
        
        
    
};